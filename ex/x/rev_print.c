#include <unistd.h>

void	rev_printf(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	while (i > 0)
	{
		write(1, &str[i - 1], 1);
		i--;
	}
}

int main()
{
	rev_printf("Vardan");
}
