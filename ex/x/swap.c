void	swap(int *a, int *b)
{
	int i;

	i = *a;
	*a = *b;
	*b = i;
}

#include <stdio.h>
int main()
{
	int x = 5;
	int y = 17;
	printf("%d %d\n", x, y);
	swap(&x, &y);
	printf("%d %d\n", x, y);
}
